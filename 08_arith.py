# Operadores básicos con int
print(10 + 10)
print(10 - 6)
print(10 * 3)
print(10 / 2)
print(10 % 3) # devuelve el residuo de la división
print(10 // 3) # devuelve n° entero de la division
print(2 ** 5) # exponenciación
# Operadores con string
print('Hola ' + 'mundo') # concatena
print('Hola ' * 3) # replica la cadena

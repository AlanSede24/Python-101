# Operadores de comparación con números
print("Op. menor que ->",7 < 9) # devuelve un boolean
print("Op. menor o igual que ->",6 <= 9)
print("Op. mayor o igual que ->",1 >= 5)
print("Op. igual que ->",2 == 6)
print("Op. distinto que ->",3 != 5)
print("Op. distinto que ->",3 != 5)
# Aplicados a otros tipos de datos
